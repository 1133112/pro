package com.pojo.qin;
/**
 * 员工职业生涯信息模型
 * @author QinGuangrui
 *
 */
public class Emp_job_info {
	private String eno;				//员工编号
	private String begainDate;		//开始日期
	private String deadline;		//截止日期
	private String company;			//所在单位
	private String jobInfo;			//工作内容
	private String job;				//担任职务
	private String sal;				//薪水
	private String reterence;		//证明人
	private String re_job;			//证明人职务
	private String re_tel;			//证明人电话
	private String remarks;			//备注
	public String getEno() {
		return eno;
	}
	public void setEno(String eno) { 
		this.eno = eno;
	}
	public String getBegainDate() {
		return begainDate;
	}
	public void setBegainDate(String begainDate) {
		this.begainDate = begainDate;
	}
	public String getDeadline() {
		return deadline;
	}
	public void setDeadline(String deadline) {
		this.deadline = deadline;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getJobInfo() {
		return jobInfo;
	}
	public void setJobInfo(String jobInfo) {
		this.jobInfo = jobInfo;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getSal() {
		return sal;
	}
	public void setSal(String sal) {
		this.sal = sal;
	}
	public String getReterence() {
		return reterence;
	}
	public void setReterence(String reterence) {
		this.reterence = reterence;
	}
	public String getRe_job() {
		return re_job;
	}
	public void setRe_job(String re_job) {
		this.re_job = re_job;
	}
	public String getRe_tel() {
		return re_tel;
	}
	public void setRe_tel(String re_tel) {
		this.re_tel = re_tel;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	
	
}
